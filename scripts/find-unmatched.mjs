async function findUnmatched() {
  const snapshot = await getDocs(collection(db, 'products'));
  const notFound = [];

  for (const docSnap of snapshot.docs) {
    const data = docSnap.data();
    const productName = (data.name || '').toUpperCase();
    
    let matched = false;
    for (const rule of priceRules) {
      const matchCount = rule.keys.filter(key => productName.includes(key.toUpperCase())).length;
      
      if (matchCount >= Math.max(2, rule.keys.length - 1)) {
        matched = true;
        break;
      }
    }
    
    if (!matched) {
      notFound.push(data.name);
    }
  }

  console.log('\n=== Products NOT Matched (40 products) ===\n');
  notFound.forEach((name, i) => console.log(`${i+1}. ${name}`));
  
  process.exit(0);
}

